package constructorss;

// constructors are similar to methods it doesn't have return type


// we use parameters for creating different types of constructors

// default constructors does not add any parameters     implicitly those are parameter less constructors


// once we use the parameter constructor it is our responsibility to create parameter less constructors
public class Constructors1 {
	public int r;

	public static void main(String[] args) {
	
		Constructors1 gh = new Constructors1();
		System.out.println(gh.r);
		System.out.println(r);
	}}
